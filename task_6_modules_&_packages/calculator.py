
def calculator(a,b,operation):
    if operation=="add":
        print("Addition is : ",a+b)
    elif operation=="sub":
        print("Substraction is : ",a-b)
    elif operation=="multiply":
        print("Multiplaction is : ",a*b)
    elif operation=="division":
        print("Division is : ",a/b)

calculator(4,5,"sub")
print(calculator)
